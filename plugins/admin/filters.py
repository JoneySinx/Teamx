async def is_admin(_, __, obj):
    """
    Works for both Message & CallbackQuery
    """
    user = None

    if isinstance(obj, Message):
        user = obj.from_user
    elif isinstance(obj, CallbackQuery):
        user = obj.from_user

    return bool(user and user.id in ADMINS)


admin_filter = filters.create(is_admin)


